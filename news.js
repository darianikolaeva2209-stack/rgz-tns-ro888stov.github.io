// Функция экранирования HTML
function escapeHtml(str) {
    if (!str) return '';
    return str
        .replace(/&/g, '&amp;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;')
        .replace(/"/g, '&quot;')
        .replace(/'/g, '&#39;');
}

// Функция отрисовки новостей
function renderNews() {
    let container = document.getElementById('news-list');
    if (!container) return;

    let html = [];

    for (let i = 0; i < newsList.length; i++) {
        let news = newsList[i];

        html.push(`
            <div class="news-card" data-news-index="${i}">
                <div class="news-date">${escapeHtml(news.date)}</div>
                <div class="news-title">${escapeHtml(news.title)}</div>
                <div class="news-preview">${escapeHtml(news.preview)}</div>
                <div class="news-full" style="display: none;">${escapeHtml(news.full)}</div>
                <button class="news-button" data-index="${i}">Показать полностью</button>
            </div>
        `);
    }

    container.innerHTML = html.join('');

    // Добавляем обработчики на кнопки
    document.querySelectorAll('.news-button').forEach(button => {
        button.addEventListener('click', function () {
            let card = this.closest('.news-card');
            let index = parseInt(card.dataset.newsIndex);
            toggleNews(index);
        });
    });
}

// Функция раскрытия/скрытия новости
function toggleNews(index) {
    let card = document.querySelector(`.news-card[data-news-index="${index}"]`);
    if (!card) return;

    let preview = card.querySelector('.news-preview');
    let full = card.querySelector('.news-full');
    let button = card.querySelector('.news-button');

    if (full.style.display === 'block') {
        full.style.display = 'none';
        preview.style.display = 'block';
        button.textContent = 'Показать полностью';
    } else {
        full.style.display = 'block';
        preview.style.display = 'none';
        button.textContent = 'Свернуть';
    }
}

// Запускаем отрисовку при загрузке страницы
document.addEventListener('DOMContentLoaded', renderNews);